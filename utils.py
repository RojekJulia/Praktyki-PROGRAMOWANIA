"""Moduł zawierający podstawowe operacje matematyczne."""


def add(a, b):
    """Zwraca sumę dwóch liczb."""
    return a + b


def subtract(a, b):
    """Zwraca różnicę dwóch liczb."""
    return a - b


def multiply(a, b):
    """Zwraca iloczyn dwóch liczb."""
    return a * b


def divide(a, b):
    """Zwraca iloraz dwóch liczb."""
    return a / b
